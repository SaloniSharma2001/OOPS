package com.prepus;

public class Animal {
    int no_of_legs;
    int no_of_eyes;
    String type;
    String Colour;

    public int getNo_of_legs() {
        return no_of_legs;
    }

    public void setNo_of_legs(int no_of_legs) {
        this.no_of_legs = no_of_legs;
    }

    public int getNo_of_eyes() {
        return no_of_eyes;
    }

    public void setNo_of_eyes(int no_of_eyes) {
        this.no_of_eyes = no_of_eyes;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getColour() {
        return Colour;
    }

    public void setColour(String colour) {
        Colour = colour;
    }


}
